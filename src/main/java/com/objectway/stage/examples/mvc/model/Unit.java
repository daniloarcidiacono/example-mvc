package com.objectway.stage.examples.mvc.model;

/**
 * Represents a unit measure (either of length or mass).
 */
public enum Unit {
    // Length units
    KILOMETERS,
    METERS,
    CENTIMETERS,
    MILLIMETERS,
    INCHES,
    FEET,

    // Mass units
    MILLIGRAMS,
    GRAMS,
    KILOGRAMS,
    POUNDS,
    OUNCES
}
